    public void atualizaTabela(){
        listExame.clear();//Limpa lista
        listExame.addAll(daoExame.getLista());
        int linha = listExame.size()-1;
        if (linha>=0){
            tblExame.setRowSelectionInterval(linha, linha);
        tblExame.scrollRectToVisible(tblExame.getCellRect(linha, linha, true));
        }
    }
    
    private void trataEdicao(boolean editando){
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        int linha = listExame.size()-1;
        if(linha<0){
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            txtCodigo.setText("");
            txtData.setText("");
            txtDescricao.setText("");
            txtExame.setText("");
            txtHora.setText("");
        }else{
            btnExcluir.setEnabled(!editando);
        }
        btnNovo.setEnabled(!editando);
        btnFechar.setEnabled(!editando);
        btnPrimeiro.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        btnAnterior.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        cbxPaciente.setEnabled(editando);
        cbxMedico.setEnabled(editando);
        tblExame.setEnabled(editando);
        txtData.setEditable(editando);
        txtDescricao.setEditable(editando);
        txtExame.setEditable(editando);
        txtHora.setEditable(editando);
    }
    
    public boolean validaCampos(){
        if(!(txtExame.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe o nome do Exame");
            txtExame.requestFocus();
            return false;
        }
        if(!(txtDescricao.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe a descrição");
            txtDescricao.requestFocus();
            return false;
        }
        if(!(txtHora.getText().length()>0)){
            JOptionPane.showMessageDialog(null, "Informe a hora");
            txtHora.requestFocus();
            return false;
        }
        if(!(cbxPaciente.getSelectedIndex()>=0)){
            JOptionPane.showMessageDialog(null, "Seleciona um paciente");
            cbxPaciente.requestFocus();
            return false;
        }
        if(!(cbxMedico.getSelectedIndex()>=0)){
            JOptionPane.showMessageDialog(null, "Seleciona um medico");
            cbxMedico.requestFocus();
            return false;
        }
        if(txtData.getText().length()>0){
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);
            try{
                sdf.parse(txtData.getText());
            } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Informe a data");
            txtData.requestFocus();
            return false;
        }
        }
         return true;
    }
    
